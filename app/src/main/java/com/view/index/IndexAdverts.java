public class IndexAdverts {
    private String id;
    private String advert_pic;
    private String advert_name;
    private String advert_url;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getAdvert_pic() {
        return advert_pic;
    }

    public void setAdvert_pic(String advert_pic) {
        this.advert_pic = advert_pic;
    }

    public String getAdvert_name() {
        return advert_name;
    }

    public void setAdvert_name(String advert_name) {
        this.advert_name = advert_name;
    }

    public String getAdvert_url() {
        return advert_url;
    }

    public void setAdvert_url(String advert_url) {
        this.advert_url = advert_url;
    }
}
